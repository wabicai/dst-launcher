async function collectLocalStats(diskPath: string): Promise<ServerStats> {
  const memTotal = os.totalmem();
  const memFree = os.freemem();
  const loadAvg = os.loadavg();
  const cpuCount = os.cpus().length;

  let swapTotal = 0;
  let swapUsed = 0;
  try {
    const meminfo = await fs.readFile('/proc/meminfo', 'utf8');
    const swapTotalKb = Number(/SwapTotal:\s+(\d+)/.exec(meminfo)?.[1] ?? 0);
    const swapFreeKb = Number(/SwapFree:\s+(\d+)/.exec(meminfo)?.[1] ?? 0);
    swapTotal = swapTotalKb * 1024;
    swapUsed = (swapTotalKb - swapFreeKb) * 1024;
  } catch {
    // non-Linux: no swap info
  }

  const dfResult = await runCommand('df', ['-Pk', diskPath]);
  let diskTotal = 0;
  let diskUsed = 0;
  let resolvedDiskPath = diskPath;
  if (dfResult.ok) {
    const lines = dfResult.stdout.trim().split('\n');
    const dataLine = lines[1]?.trim().split(/\s+/) ?? [];
    diskTotal = Number(dataLine[1] ?? 0) * 1024;
    diskUsed = Number(dataLine[2] ?? 0) * 1024;
    resolvedDiskPath = dataLine[5] ?? diskPath;
  }

  return {
    cpu: { loadAvg1: loadAvg[0] ?? 0, cpuCount },
    memory: { total: memTotal, used: memTotal - memFree },
    swap: { total: swapTotal, used: swapUsed },
    disk: { total: diskTotal, used: diskUsed, path: resolvedDiskPath },
    collectedAt: new Date().toISOString(),
  };
}

async function collectRemoteStats(target: import('@dst-launcher/shared').SshTargetConfig): Promise<ServerStats> {
  const sshArgs = [
    '-i', target.privateKeyPath,
    '-p', String(target.port),
    '-o', 'BatchMode=yes',
    `${target.username}@${target.host}`,
    `uptime && echo '---CPUS---' && nproc && echo '---MEM---' && cat /proc/meminfo && echo '---DF---' && df -Pk ${target.remotePath || '/'}`,
  ];

  const result = await runCommand('ssh', sshArgs);
  if (!result.ok) {
    return {
      cpu: { loadAvg1: 0, cpuCount: 0 },
      memory: { total: 0, used: 0 },
      swap: { total: 0, used: 0 },
      disk: { total: 0, used: 0, path: target.remotePath || '/' },
      collectedAt: new Date().toISOString(),
    };
  }

  const out = result.stdout;
  const loadMatch = /load average:\s*([\d.]+)/.exec(out);
  const loadAvg1 = Number(loadMatch?.[1] ?? 0);

  const cpusPart = out.split('---CPUS---')[1]?.split('---MEM---')[0] ?? '';
  const cpuCount = Number(cpusPart.trim()) || 0;

  const memPart = out.split('---MEM---')[1]?.split('---DF---')[0] ?? '';
  const dfPart = out.split('---DF---')[1] ?? '';

  const memTotalKb = Number(/MemTotal:\s+(\d+)/.exec(memPart)?.[1] ?? 0);
  const memAvailKb = Number(/MemAvailable:\s+(\d+)/.exec(memPart)?.[1] ?? 0);
  const swapTotalKb = Number(/SwapTotal:\s+(\d+)/.exec(memPart)?.[1] ?? 0);
  const swapFreeKb = Number(/SwapFree:\s+(\d+)/.exec(memPart)?.[1] ?? 0);

  const dfLines = dfPart.trim().split('\n');
  const dfData = dfLines[1]?.trim().split(/\s+/) ?? [];
  const diskTotal = Number(dfData[1] ?? 0) * 1024;
  const diskUsed = Number(dfData[2] ?? 0) * 1024;
  const diskPath = dfData[5] ?? target.remotePath ?? '/';

  return {
    cpu: { loadAvg1, cpuCount },
    memory: { total: memTotalKb * 1024, used: (memTotalKb - memAvailKb) * 1024 },
    swap: { total: swapTotalKb * 1024, used: (swapTotalKb - swapFreeKb) * 1024 },
    disk: { total: diskTotal, used: diskUsed, path: diskPath },
    collectedAt: new Date().toISOString(),
  };
}
